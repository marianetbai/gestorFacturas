    private void actualizarContraseñaEnBD(String usuario, String nuevaContrasenia) {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("persistence");
        UsuarioJpaController ujpa = new UsuarioJpaController(emf);

        try {
            //busco el usuario en la base de datos
            Usuario usuarioBD = ujpa.findUsuarioByUsuario(usuario);

            if (usuarioBD != null) {
                //actualizo la contraseña
                usuarioBD.setPassword(nuevaContrasenia);

                //uso edit para los cambios en la base de datos
                ujpa.edit(usuarioBD);
            } else {
                mostrarMensaje("User not found");
            }
        } catch (Exception ex) {
            mostrarMensaje("Error updating the password: " + ex.getMessage());
        } finally {
            emf.close();
        }
    }
    
    private void mostrarMensaje(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje);
    }
    
