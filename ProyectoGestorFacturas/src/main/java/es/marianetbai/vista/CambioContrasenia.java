    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        //obtenemos el usuario del campo de texto
        usuarioActual = txtUsuarioABuscar.getText();

        //obtenemos la nueva contraseña y la confirmación del campo de texto
        nuevaContrasenia = txtConfirmarCon.getText();

        //verificamos que se haya proporcionado un usuario y una nueva contraseña
        if (!usuarioActual.isEmpty() && !nuevaContrasenia.isEmpty()) {
            //actualizamos la contraseña en la base de datos
            actualizarContraseñaEnBD(usuarioActual, nuevaContrasenia);
            mostrarMensaje("Password updated successfully");

            //cierro la ventana actual (CambioContrasenia)
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
            frame.dispose();

            //y abro la ventana del Login
            JFrame loginFrame = new JFrame("Log in");
            loginFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            loginFrame.getContentPane().add(new Login());
            loginFrame.pack();
            loginFrame.setVisible(true);
            loginFrame.setLocationRelativeTo(null);
        } else {
            mostrarMensaje("Please complete all the fields");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

